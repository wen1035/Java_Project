public class Player {
	private int x;
	private int y;
	//存入棋子的X,Y的位置
	public Player(int x, int y) 
	{
		super();
		this.x = x;
		this.y = y;
	}
	public int getX() 
	{
		return x;
	}
	public void setX(int x) 
	{
		this.x = x;
	}
	public int getY() 
	{
		return y;
	}
	public void setY(int y)
	{
		this.y = y;
	}
}

